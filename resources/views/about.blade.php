                <h2>Information Collection and Use</h2>
                <h3>Personal Information</h3>
                    Only the chat_id and user id of the telegram is stored on IOTEL server per user. Phone numbers and usernames are not used or stored.
                <h3>Instagram</h3>
                    By authorizing IOTEL Bot to access your instagram account, IOTEL Bot can access your user and media information. Only instagram id and last uploaded media id will be stored on IOTEL server and no media is stored on the server. In case of a new upload to your instagram account, a link to newly uploaded media is sent to all of your bot users. If a bot user is not your instagram follower, the  link would be inaccessible.
                <h3>Broadcasting messages to bot users</h3>
                    Any messages sent to bot users by bot owner will be directly sent to users and no information (text, photo , file_id ,…)  will be stored on IOTEL server.
